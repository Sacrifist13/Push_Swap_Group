#include "../libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		letter;
	long int	nbr;

	nbr = (long int)n;
	if (nbr < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = -nbr;
	}
	if (nbr > 9)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	else
	{
		letter = nbr + 48;
		ft_putchar_fd(letter, fd);
	}
	return ;
}
