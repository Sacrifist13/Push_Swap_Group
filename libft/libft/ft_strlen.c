#include "../libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	size;
	int		i;

	i = 0;
	size = 0;
	if (s == NULL)
		return (0);
	while (s[i] != '\0')
	{
		i++;
		size++;
	}
	return (size);
}
